/// <summary>
/// Represents a collection of predefined color palettes.
/// Each member of this enumeration corresponds to a specific color
/// that can be mapped via the <see cref="PalettesManager"/> class.
/// </summary>
public enum Palettes
{
    Blue,
    Red,
    Black,
    LightBlue,
    Brown,
    Green,
    Turquoise,
    Pink,
    Purple,
    Tan,
    White,
    Yellow
}